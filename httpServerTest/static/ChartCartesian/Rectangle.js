
class Rectangle{

    //-------------------------------------------------------------------------
    constructor() {
        this.mX1 = 0.0;
        this.mY1 = 0.0;
        this.mX2 = 0.0;
        this.mY2 = 0.0;
    }
    
    //-------------------------------------------------------------------------
    getCenter() {
        return [
            this.mX1 + (this.mX2 - this.mX1) * 0.5,
            this.mY1 + (this.mY2 - this.mY1) * 0.5];
    }

    //-------------------------------------------------------------------------
    getHeight() {
        return this.mY2 - this.mY1;
    }

    //-------------------------------------------------------------------------
    getSize() {
        return [this.mX2 - this.mX1, this.mY2 - this.mY1];
    }

    //-------------------------------------------------------------------------
    getWidth() {
        return this.mX2 - this.mX1;
    }

    //-------------------------------------------------------------------------
    isValid() {
        var s = this.getSize();
        return s[0] > 0.0 && s[1] > 0.0; 
    }
    
    //-------------------------------------------------------------------------
    setCorners(x1, y1, x2, y2){
        this.mX1 = x1;
        this.mY1 = y1;
        this.mX2 = x2;
        this.mY2 = y2;
    }
}